n = input('Informe um valor: ')
print(n.isupper()) # verifica se a string é toda maiuscula
print(n.isspace()) # verifica se é espaço
print(n.isdigit()) # verifica se é digito int
print(n.isalnum()) # verifica se é alfanumerico
print(n.isalpha()) # verifica se é afabetico
print(n.isdecimal()) # verifica se é decimal
print(n.islower()) # verifica se a string é toda minuscula
print(n.isidentifier()) # verifica se é valido como parametro
print(n.istitle()) # verifica se possui maiuscula no inicio da palavra e minuscula no corpo
print(n.isprintable()) #verifica se possui caracteres imprimiveis